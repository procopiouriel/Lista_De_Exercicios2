public class Conta
{
    public double Soma(double soma, double soma1)
    {
        return soma + soma1;
    }
    public double Subtracao(double subtracao, double subtracao1)
    {
        return subtracao - subtracao1;
    }

    public double Multiplicacao(double multiplicacao, double multiplicacao1)
    {
        return multiplicacao * multiplicacao1;
    }
    public double Divisao(double divisao, double divisao1)
    {
        return divisao / divisao1;
    }
}